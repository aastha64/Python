# Operators in Python

x = 15
y = 6

# Arithmetic Operations
print("Addition:", x + y)       # Adds x and y
print("Subtraction:", x - y)    # Subtracts y from x
print("Multiplication:", x * y) # Multiplies x by y
print("Division:", x / y)       # Divides x by y (float result)
print("Floor Division:", x // y) # Integer division (quotient without remainder)
print("Exponentiation:", x ** y) # x raised to the power y
